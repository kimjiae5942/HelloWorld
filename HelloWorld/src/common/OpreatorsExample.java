package common;

public class OpreatorsExample {
    public static void main(String[] args) {
	int var1 = 100;
	byte var2 = 50;
	int result = var1 + var2;
	System.out.println(var1 + "과" + var2 + "의 합은 " + result + " 입니다.");
    float f1 = 10.1F;
    float f2 = 5.5F;
    float f3 = f1 + f2;
    System.out.println(f1 + "과" + f2 + "의 합은 " + f3 + " 입니다.");
}
}